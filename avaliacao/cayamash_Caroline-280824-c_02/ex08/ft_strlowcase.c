#include <stdio.h>
char	*ft_strlowcase(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		if (str[n] >= 'A' && str[n] <= 'Z')
			str[n] += 32;
		n++;
	}
	return (str);
}

int	main()
{
	char	str[20] = "hAMBURGUER hOJE!";
	
	ft_strlowcase(str);
	printf("%s", str);
	return(0);
}

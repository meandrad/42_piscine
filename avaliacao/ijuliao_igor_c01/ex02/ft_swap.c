#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	u;

	u = *a;
	*a = *b;
	*b = u;
}

int	main(void)
{
	int i;
	int g;
	i = 5;
	g = 10;
	ft_swap(&i, &g);

	printf("%d %d", i, g);
}

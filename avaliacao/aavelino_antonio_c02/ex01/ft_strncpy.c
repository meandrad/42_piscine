#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main(void)
{
	char	dest[20] = "####################";
	char	souce[] = "Sao Paulo";
	int		valor;

	valor = 4;
	ft_strncpy(dest, souce, valor);
	printf("String_%s\n", dest);
	return (0);
}
